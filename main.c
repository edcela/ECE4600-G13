/* USER CODE BEGIN 4 */
void Handle_Input()
{
	CarCommand command;
	int8_t input = atoi((const char*)RxBuf);
	switch(input)
	{
		case 0:
			command = CMD_STOP;
			break;
		case 1:
			command = CMD_FORWARD;
			break;
		case 2:
			command = CMD_LEFT;
			break;
		case 3:
			command = CMD_RIGHT;
			break;
		default:
			command = CMD_INVALID;
			break;
	}
	ControlCar(command);
}

void ControlCar(CarCommand command) {
    switch (command) {
        case CMD_STOP: // Stop
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 0);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 0);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 0);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, 0);
            break;
        case CMD_FORWARD: // Move forward
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, HIGH_SPEED); // Left Motors
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, HIGH_SPEED);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, HIGH_SPEED); // Right Motors
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, HIGH_SPEED);
            break;
        case CMD_LEFT: // Turn right (right side slower)
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, HIGH_SPEED); // Left Motors faster
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, HIGH_SPEED);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, LOW_SPEED); // Right Motors slower
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, LOW_SPEED);
            break;
        case CMD_RIGHT: // Turn left (left side slower)
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, LOW_SPEED); // Left Motors slower
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, LOW_SPEED);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, HIGH_SPEED); // Right Motors faster
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, HIGH_SPEED);
            break;
        default:
            // Invalid command, stop the car
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 0);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, 0);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 0);
            __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, 0);
            break;
    }
}

